    public String getDept(int department)
    {
        String dept = "";
        
        switch(department)
        {
            case 1: //Computer Science
                dept = "CSC";
                break;
            case 2:
                dept = "MAT";
                break;
            case 3:
                dept = "PHY";
                break;
            default:
                break;
        }
        return dept;
    }
