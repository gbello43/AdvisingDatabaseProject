    public void setLowerDivision()
    {

        DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
        
        Object[] row = new Object[8];
        
        
        AdvisingDB db = new AdvisingDB();
        ResultSet course = db.getCourseDivision(1);
       
        try {
            row[0] = getDept(course.getInt("departmentID"));
            row[1] = course.getInt("courseID"); //courseName
            row[2] = course.getString("courseName");
            row[3] = course.getInt("units");    //units
            row[4] = course.getString("preReqID");
            
            table.addRow(row);
            
            while(course.next())
            {
                row[0] = getDept(course.getInt("departmentID"));

                row[1] = course.getInt("courseID"); //courseName
                row[2] = course.getString("courseName");
                row[3] = course.getInt("units");    //units
                row[4] = course.getString("preReqID");
                table.addRow(row);
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdvisingDB.class.getName()).log(Level.SEVERE, null, ex);
        }       
    }
